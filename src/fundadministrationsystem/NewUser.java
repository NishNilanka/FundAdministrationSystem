    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!pwdPassword.getText().equals(pwdConfirmPassword.getText())){
            JOptionPane.showMessageDialog(this, "These passwords don't match. Try again!");
            //System.out.println(pwdPassword.getText());
        }
        else{
        UserDetails details = new UserDetails();
        
        
        details.setUserNo(0);
        details.setNic(txtNIC.getText()+ (String)cbNIC.getSelectedItem());
        details.setFirstName(txtFirstName.getText());
        details.setLastName(txtLastName.getText());
        details.setDesignation((String)cbDesignation.getSelectedItem());
        details.setUserType((String)cbUserType.getSelectedItem());
        details.setAddress(txtAddress.getText());
        details.setContactNo(Integer.parseInt(txtPhoneNo.getText()));
        details.setMember(cbMember.isSelected());
        if ("".equals(txtAccountNo.getText()))
            details.setAccountNo(0);
        else
            details.setAccountNo(Integer.parseInt(txtAccountNo.getText()));
            try {
                String encryptPassword = EncryDecryp.encrypt(pwdPassword.getText());
                details.setPassword(encryptPassword);
            } catch (Exception ex) {
                System.out.println(ex);
            }
        
        details.setUserName(txtUserName.getText());
        
        boolean result = DBoperation.addUser(details);
        if(result)
        {
            JOptionPane.showMessageDialog(this, "Successfully inserted");
            clearFields();
        }
        else
            JOptionPane.showMessageDialog(this, "Error occurs while inserting");
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

