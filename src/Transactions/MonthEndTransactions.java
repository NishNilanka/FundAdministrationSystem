    private void btnStartTransactionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartTransactionActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(this, "Are you sure, you want to do this operation ?", "Month-End Transactions", JOptionPane.YES_NO_OPTION);
        
        if (result == 0) {
            userID = new Integer(txtUserId.getText());
            newMembers  = new ArrayList<TransactionDetail>();
            year = new Integer(txtYear.getText());
            month = new Integer(txtMonth.getText());
            DBoperations db = new DBoperations();
                       
            for(int r = 0; r < Members.size(); r++)
            {
                td = new TransactionDetail();
                td.setYear(year);
                td.setMonth(month);
                td.setEmpId(new Integer(tblTransaction.getValueAt(r, 0).toString()));
                td.setName(tblTransaction.getValueAt(r, 1).toString());
                td.setLedgerNo(tblTransaction.getValueAt(r, 2).toString());
                td.setFixedDeposit(new Float(tblTransaction.getValueAt(r, 3).toString()));
                td.setScholorship(new Float(tblTransaction.getValueAt(r, 4).toString()));
                td.setRegFee(new Float(tblTransaction.getValueAt(r, 5).toString()));
                td.setLoan(new Float(tblTransaction.getValueAt(r, 6).toString()));
                td.setDeathGrant(new Float(tblTransaction.getValueAt(r, 7).toString()));
                td.setDeathGrantLoan(new Float(tblTransaction.getValueAt(r, 8).toString()));
                td.setTotal(new Float(tblTransaction.getValueAt(r, 9).toString()));
                td.setUserID(userID);

                newMembers.add(td);
            }
            db.monthEndTransactions(newMembers);
        }
    }//GEN-LAST:event_btnStartTransactionActionPerformed

