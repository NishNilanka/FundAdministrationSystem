    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        MemberDetails ed = new MemberDetails();


        try{
        ed.setFirstName(txtFirstName.getText());
        ed.setLastName(txtLastName.getText());
        ed.setNicNo(txtNICNo.getText()+(String)cbNIC.getSelectedItem());

        ed.setAddress(txtAddress.getText());

        ed.setGender((String)cbGender.getSelectedItem());
        ed.setDesignation((String)cbDesignation.getSelectedItem());
        ed.setRegDate(dateFormat.format(cbRegisteredDate.getDate()));
        ed.setPrison((String) cbPrison.getSelectedItem());
        ed.setPhoneNo(Integer.parseInt(txtPhoneNo.getText()));
        ed.setLedgerNo(txtLedgerNo.getText());
        ed.setRetirementDate(dateFormat.format(cbRitermentDate.getDate()));
        
        
        ed.setOwnerName(txtOwnerName.getText());
        ed.setOwnerNIC(txtOwnerNIC.getText()+(String)cbNICOwner.getSelectedItem());
        ed.setRelationship(txtRelationship.getText());
        
        ed.setAccountNo(txtLedgerNo.getText());
        BigDecimal balance = new BigDecimal(0.0);
        ed.setCurrentBalance(balance);
        
        
        

        boolean result = DBoperation.addMember(ed);
       // boolean resultOwner = DBoperation.addOwnerTransfer(ownershipDetails);
        if(result)
        {
            JOptionPane.showMessageDialog(this, "Successfully inserted");
            clearFields();
        }
        else
            JOptionPane.showMessageDialog(this, "Error occurs while inserting");
        }catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Member details are not valid");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

