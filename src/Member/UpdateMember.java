    public void setFields(MemberDetails member)
    {
        member1 = member;
        txtEmpNo.setText(new Integer(member.getEmpno()).toString());
        txtFirstName.setText(member.getFirstName());
        txtLastName.setText(member.getLastName());
        txtNICNo.setText(member.getNicNo().substring(0, 9));
        cbNIC.setSelectedItem(member.getNicNo().substring(9));
        //txtAge.setText(new Integer(member.getAge()).toString());
        txtAddress.setText(member.getAddress());
        //date = new java.util.Date(member.getDob());
        //cbDOB.setDate(date);
        cbGender.setSelectedItem(member.getGender());
        date = new java.util.Date(member.getRegDate());
        cbRegisteredDate.setDate(date);
        cbDesignation.setSelectedItem(member.getDesignation());
        cbPrison.setSelectedItem(member.getPrison());
        txtPhoneNo.setText(new Integer(member.getPhoneNo()).toString());
        txtLedgerNo.setText(member.getLedgerNo());
        date = new java.util.Date(member.getRetirementDate());
        cbRitermentDate.setDate(date);
        
        txtOwnerName.setText(member.getOwnerName());
        txtOwnerNIC.setText(member.getOwnerNIC().substring(0, 9));
        cbNICOwner.setSelectedItem(member.getOwnerNIC().substring(9));
        txtRelationship.setText(member.getRelationship());
        
    
    
    }
    
    
    
    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        MemberDetails em = new MemberDetails();
        
        em.setEmpno(Integer.parseInt(txtEmpNo.getText().toString()));
        em.setFirstName(txtFirstName.getText());
        em.setLastName(txtLastName.getText());
        em.setNicNo(txtNICNo.getText() + (String)cbNIC.getSelectedItem());
        //em.setAge(Integer.parseInt(txtAge.getText().toString()));
        em.setAddress(txtAddress.getText());
        //em.setDob(cbDOB.getDate().toString());
        em.setLedgerNo(txtLedgerNo.getText());
        em.setGender((String)cbGender.getSelectedItem());
        em.setDesignation((String)cbDesignation.getSelectedItem());
        em.setRegDate(dateFormat.format(cbRegisteredDate.getDate()));
        em.setPrison((String)cbPrison.getSelectedItem());
        em.setRetirementDate(dateFormat.format(cbRitermentDate.getDate()));
        
        em.setOwnerID(member1.getOwnerID());
        em.setOwnerName(txtOwnerName.getText());
        em.setOwnerNIC(txtOwnerNIC.getText());
        em.setRelationship(txtRelationship.getText());
        
         
        boolean result = db.updateMember(em);
        
        if(result){
            JOptionPane.showMessageDialog(this, "Pending for authorization...");
            this.dispose();
            
        }
        
        else{
            JOptionPane.showMessageDialog(this,"Sorry,Error occured while updating");
            
        }
        
        
             
    }//GEN-LAST:event_btnSubmitActionPerformed

        public boolean isAlpha(String name) {
    char[] chars = name.toCharArray();

    for (char c : chars) {
        if(!Character.isLetter(c)) {
            return false;
        }
    }

    return true;
}
 
    
