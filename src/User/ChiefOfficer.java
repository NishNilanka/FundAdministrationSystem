    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String EmpNO = txtEmpNo.getText();
        String NIC = "";
        if (!txtNIC.getText().equals("")) {
            NIC = txtNIC.getText() + (String) cbNIC.getSelectedItem();
        }
        String Name = txtName.getText();
        String prison = (String) cbPrison.getSelectedItem();

        if (EmpNO.equals("") && NIC.equals("") && Name.equals("")) {
            members = DBWithFinal.getMemberByPrison(prison);
            Member memberList = new Member(members);
            tblMember.setModel(memberList);
        } else if (EmpNO.equals("") && NIC.equals("")) {
            members = DBWithFinal.getMemberByName(Name, prison);
            Member memberList = new Member(members);
            tblMember.setModel(memberList);
        } else if (EmpNO.equals("") && Name.equals("")) {
            members = DBWithFinal.getMemberByNIC(NIC, prison);
            Member memberList = new Member(members);
            tblMember.setModel(memberList);
        } else if (Name.equals("") && NIC.equals("")) {
            members = DBWithFinal.getMemberByEmpNo(EmpNO, prison);
            Member memberList = new Member(members);
            tblMember.setModel(memberList);
        } else if (NIC.equals("")) {
            members = DBWithFinal.getMemberByEmpNoAndName(EmpNO, prison, Name);
            Member memberList = new Member(members);
            tblMember.setModel(memberList);
        } else if (EmpNO.equals("")) {
            members = DBWithFinal.getMemberByNICAndName(NIC, prison, Name);
            Member memberList = new Member(members);
            tblMember.setModel(memberList);
        } else if (Name.equals("")) {
            members = DBWithFinal.getMemberByEmpNoAndNIC(EmpNO, prison, NIC);
            Member memberList = new Member(members);
            tblMember.setModel(memberList);
        } else {
            members = DBWithFinal.getMemberByEmpNoAndNICAndName(EmpNO, prison, NIC, Name);
            Member memberList = new Member(members);
            tblMember.setModel(memberList);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

